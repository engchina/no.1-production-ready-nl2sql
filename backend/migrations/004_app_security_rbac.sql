-- Application-local authentication, RBAC, sessions, audit, and DeepSec ledger.
-- Apply explicitly before APP_AUTH_ENABLED=true startup.

CREATE TABLE NL2SQL_APP_USERS (
    USER_ID VARCHAR2(36) PRIMARY KEY,
    LOGIN_NAME VARCHAR2(64) NOT NULL,
    LOGIN_NAME_NORMALIZED VARCHAR2(64) NOT NULL,
    DISPLAY_NAME VARCHAR2(256) NOT NULL,
    PASSWORD_HASH VARCHAR2(512) NOT NULL,
    STATUS VARCHAR2(16) DEFAULT 'ACTIVE' NOT NULL,
    FORCE_PASSWORD_CHANGE NUMBER(1) DEFAULT 1 NOT NULL,
    FAILED_LOGIN_COUNT NUMBER(10) DEFAULT 0 NOT NULL,
    LOCKED_UNTIL TIMESTAMP WITH TIME ZONE,
    VERSION_NO NUMBER(19) DEFAULT 1 NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_NL2SQL_APP_USERS_STATUS CHECK (STATUS IN ('ACTIVE', 'DISABLED')),
    CONSTRAINT CK_NL2SQL_APP_USERS_FORCE_PWD CHECK (FORCE_PASSWORD_CHANGE IN (0, 1))
);

CREATE UNIQUE INDEX UX_NL2SQL_APP_USERS_LOGIN ON NL2SQL_APP_USERS (LOGIN_NAME_NORMALIZED);

CREATE TABLE NL2SQL_APP_ROLES (
    ROLE_ID VARCHAR2(36) PRIMARY KEY,
    ROLE_CODE VARCHAR2(64) NOT NULL,
    DISPLAY_NAME VARCHAR2(256) NOT NULL,
    DESCRIPTION VARCHAR2(1000) DEFAULT '-' NOT NULL,
    IS_BUILT_IN NUMBER(1) DEFAULT 0 NOT NULL,
    ARCHIVED NUMBER(1) DEFAULT 0 NOT NULL,
    VERSION_NO NUMBER(19) DEFAULT 1 NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_NL2SQL_APP_ROLES_FLAGS CHECK (
        IS_BUILT_IN IN (0, 1) AND ARCHIVED IN (0, 1)
    )
);

CREATE UNIQUE INDEX UX_NL2SQL_APP_ROLES_CODE ON NL2SQL_APP_ROLES (ROLE_CODE);

CREATE TABLE NL2SQL_APP_USER_ROLES (
    USER_ID VARCHAR2(36) NOT NULL,
    ROLE_ID VARCHAR2(36) NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_NL2SQL_APP_USER_ROLES PRIMARY KEY (USER_ID, ROLE_ID),
    CONSTRAINT FK_NL2SQL_APP_UR_USER FOREIGN KEY (USER_ID) REFERENCES NL2SQL_APP_USERS (USER_ID),
    CONSTRAINT FK_NL2SQL_APP_UR_ROLE FOREIGN KEY (ROLE_ID) REFERENCES NL2SQL_APP_ROLES (ROLE_ID)
);

CREATE TABLE NL2SQL_APP_ROLE_PERMISSIONS (
    ROLE_ID VARCHAR2(36) NOT NULL,
    PERMISSION_CODE VARCHAR2(128) NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_NL2SQL_APP_ROLE_PERMISSIONS PRIMARY KEY (ROLE_ID, PERMISSION_CODE),
    CONSTRAINT FK_NL2SQL_APP_RP_ROLE FOREIGN KEY (ROLE_ID) REFERENCES NL2SQL_APP_ROLES (ROLE_ID)
);

CREATE TABLE NL2SQL_APP_DATA_ENTITLEMENTS (
    ENTITLEMENT_ID VARCHAR2(36) PRIMARY KEY,
    ROLE_ID VARCHAR2(36) NOT NULL,
    RESOURCE_CODE VARCHAR2(128) NOT NULL,
    SCOPE_CODE VARCHAR2(256) NOT NULL,
    CAPABILITY VARCHAR2(64) NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_NL2SQL_APP_DE_ROLE FOREIGN KEY (ROLE_ID) REFERENCES NL2SQL_APP_ROLES (ROLE_ID),
    CONSTRAINT UX_NL2SQL_APP_DE UNIQUE (ROLE_ID, RESOURCE_CODE, SCOPE_CODE, CAPABILITY)
);

CREATE TABLE NL2SQL_AUTH_SESSIONS (
    SESSION_ID VARCHAR2(36) PRIMARY KEY,
    USER_ID VARCHAR2(36) NOT NULL,
    TOKEN_HASH VARCHAR2(64) NOT NULL,
    CSRF_TOKEN_HASH VARCHAR2(64) NOT NULL,
    IDLE_EXPIRES_AT TIMESTAMP WITH TIME ZONE NOT NULL,
    ABSOLUTE_EXPIRES_AT TIMESTAMP WITH TIME ZONE NOT NULL,
    LAST_SEEN_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    REVOKED_AT TIMESTAMP WITH TIME ZONE,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_NL2SQL_AUTH_SESSION_USER FOREIGN KEY (USER_ID) REFERENCES NL2SQL_APP_USERS (USER_ID)
);

CREATE UNIQUE INDEX UX_NL2SQL_AUTH_SESSION_TOKEN ON NL2SQL_AUTH_SESSIONS (TOKEN_HASH);
CREATE INDEX IX_NL2SQL_AUTH_SESSION_USER ON NL2SQL_AUTH_SESSIONS (USER_ID, REVOKED_AT);

CREATE TABLE NL2SQL_AUTH_AUDIT_LOG (
    AUDIT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACTOR_USER_ID VARCHAR2(36),
    EVENT_TYPE VARCHAR2(64) NOT NULL,
    TARGET_TYPE VARCHAR2(64) DEFAULT '-' NOT NULL,
    TARGET_ID VARCHAR2(128) DEFAULT '-' NOT NULL,
    OUTCOME VARCHAR2(16) NOT NULL,
    DETAIL_JSON CLOB CHECK (DETAIL_JSON IS JSON) NOT NULL,
    REQUEST_ID VARCHAR2(128) DEFAULT '-' NOT NULL,
    CLIENT_IP VARCHAR2(128) DEFAULT '-' NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE INDEX IX_NL2SQL_AUTH_AUDIT_TIME ON NL2SQL_AUTH_AUDIT_LOG (CREATED_AT DESC);

CREATE TABLE NL2SQL_DEEPSEC_MIGRATIONS (
    PLAN_VERSION VARCHAR2(32) NOT NULL,
    STEP_NO NUMBER(10) NOT NULL,
    STEP_KEY VARCHAR2(128) NOT NULL,
    CHECKSUM VARCHAR2(64) NOT NULL,
    STATUS VARCHAR2(16) NOT NULL,
    ERROR_MESSAGE VARCHAR2(2000) DEFAULT '-' NOT NULL,
    EXECUTED_BY VARCHAR2(36),
    EXECUTED_AT TIMESTAMP WITH TIME ZONE,
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_NL2SQL_DEEPSEC_MIGRATIONS PRIMARY KEY (PLAN_VERSION, STEP_NO),
    CONSTRAINT CK_NL2SQL_DEEPSEC_MIG_STATUS CHECK (STATUS IN ('PENDING', 'RUNNING', 'APPLIED', 'FAILED'))
);

-- Oracle treats an empty string as NULL. These ALTER statements also repair
-- installations created by an earlier revision of this migration.
ALTER TABLE NL2SQL_APP_ROLES MODIFY (DESCRIPTION DEFAULT '-');

ALTER TABLE NL2SQL_AUTH_AUDIT_LOG MODIFY (
    TARGET_TYPE DEFAULT '-',
    TARGET_ID DEFAULT '-',
    REQUEST_ID DEFAULT '-',
    CLIENT_IP DEFAULT '-'
);

ALTER TABLE NL2SQL_DEEPSEC_MIGRATIONS MODIFY (ERROR_MESSAGE DEFAULT '-');
