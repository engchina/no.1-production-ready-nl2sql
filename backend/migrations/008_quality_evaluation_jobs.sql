-- Durable, resumable NL2SQL quality evaluation jobs and attempt results.

CREATE TABLE NL2SQL_EVALUATION_JOBS (
    JOB_ID VARCHAR2(64) PRIMARY KEY,
    STATUS VARCHAR2(32) NOT NULL,
    PROFILE_ID VARCHAR2(256) NOT NULL,
    WORKER_ID VARCHAR2(256),
    HEARTBEAT_AT TIMESTAMP WITH TIME ZONE,
    LEASE_EXPIRES_AT TIMESTAMP WITH TIME ZONE,
    ATTEMPT_NO NUMBER(10) DEFAULT 0 NOT NULL,
    PAYLOAD_JSON CLOB NOT NULL CHECK (PAYLOAD_JSON IS JSON),
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_NL2SQL_EVAL_JOB_STATUS CHECK (
        STATUS IN ('pending', 'running', 'completed', 'completed_with_errors', 'failed')
    )
);

CREATE INDEX IX_NL2SQL_EVAL_JOB_STATE
    ON NL2SQL_EVALUATION_JOBS (STATUS, CREATED_AT);

CREATE INDEX IX_NL2SQL_EVAL_JOB_LEASE
    ON NL2SQL_EVALUATION_JOBS (STATUS, LEASE_EXPIRES_AT, CREATED_AT);

CREATE TABLE NL2SQL_EVALUATION_RESULTS (
    RESULT_ID VARCHAR2(64) NOT NULL,
    JOB_ID VARCHAR2(64) NOT NULL,
    CASE_NO NUMBER(10) NOT NULL,
    ENGINE VARCHAR2(32) NOT NULL,
    REPETITION_NO NUMBER(2) NOT NULL,
    RESULT_STATUS VARCHAR2(32) NOT NULL,
    VERDICT VARCHAR2(32) NOT NULL,
    PAYLOAD_JSON CLOB NOT NULL CHECK (PAYLOAD_JSON IS JSON),
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_NL2SQL_EVAL_RESULTS PRIMARY KEY (JOB_ID, RESULT_ID),
    CONSTRAINT FK_NL2SQL_EVAL_RESULTS_JOB FOREIGN KEY (JOB_ID)
        REFERENCES NL2SQL_EVALUATION_JOBS (JOB_ID) ON DELETE CASCADE,
    CONSTRAINT UX_NL2SQL_EVAL_ATTEMPT UNIQUE
        (JOB_ID, CASE_NO, ENGINE, REPETITION_NO),
    CONSTRAINT CK_NL2SQL_EVAL_RESULT_STATUS CHECK (
        RESULT_STATUS IN ('completed', 'error')
    ),
    CONSTRAINT CK_NL2SQL_EVAL_VERDICT CHECK (
        VERDICT IN ('correct', 'incorrect', 'uncertain', 'not_analyzed')
    )
);

INSERT INTO NL2SQL_SCHEMA_MIGRATIONS (VERSION_NO, DESCRIPTION, CHECKSUM)
VALUES (8, 'durable NL2SQL quality evaluation jobs', 'runtime-verified');
