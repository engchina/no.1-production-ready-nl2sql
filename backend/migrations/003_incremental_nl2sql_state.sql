    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_NL2SQL_ONT_PROFILE_VIEW_REV PRIMARY KEY (PROFILE_ID, REVISION_ID)
);

CREATE INDEX IX_NL2SQL_ONT_VIEW_REVISION
    ON NL2SQL_ONTOLOGY_PROFILE_VIEW_REVISIONS (REVISION_ID, PROFILE_ID);

