INSERT INTO NL2SQL_CHANGE_TOKENS (NAMESPACE, CHANGE_SEQ) VALUES ('profiles', 0);
INSERT INTO NL2SQL_CHANGE_TOKENS (NAMESPACE, CHANGE_SEQ) VALUES ('schema', 0);
INSERT INTO NL2SQL_CHANGE_TOKENS (NAMESPACE, CHANGE_SEQ) VALUES ('state', 0);

-- Oracle treats an empty string as NULL, so a mandatory empty-string default on a
-- populated legacy table fails with ORA-01758.  Add the column nullable,
-- recover the profile from the proposal/session payload, then enforce the
-- invariant after every legacy row has a deterministic value.
ALTER TABLE NL2SQL_ONTOLOGY_PROPOSALS
    ADD (PROFILE_ID VARCHAR2(128));

UPDATE NL2SQL_ONTOLOGY_PROPOSALS proposal
SET PROFILE_ID = COALESCE(
    JSON_VALUE(
        proposal.PAYLOAD_JSON,
        '$.profile_id' RETURNING VARCHAR2(128) NULL ON ERROR
    ),
    (
        SELECT MAX(session_row.PROFILE_ID)
        FROM NL2SQL_ONTOLOGY_QUERY_SESSIONS session_row
        WHERE session_row.SESSION_ID = proposal.SESSION_ID
    ),
    '__legacy__'
)
WHERE PROFILE_ID IS NULL;

ALTER TABLE NL2SQL_ONTOLOGY_PROPOSALS
    MODIFY (PROFILE_ID NOT NULL);

CREATE INDEX IX_NL2SQL_ONT_PROPOSAL_PROFILE
    ON NL2SQL_ONTOLOGY_PROPOSALS (PROFILE_ID, STATUS, UPDATED_AT);

